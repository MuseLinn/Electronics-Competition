//------------------------------------------------------------------------------
// Testbench for DDS Compiler Wrapper
// 【DDS 编译器封装模块的仿真测试平台】
//
// 功能说明：
//   本 testbench 用于验证 dds_compiler_wrapper 模块的功能。
//   通过仿真观察 DDS 在不同频率下的输出波形。
//
// 测试流程：
//   1. 产生 100MHz 时钟和复位信号
//   2. 复位结束后，加载 1MHz 频率，观察 100 个周期
//   3. 切换到 2MHz 频率，观察 200 个周期
//   4. 切换到 0Hz（DC），观察恒定输出
//   5. 仿真结束
//
// 仿真参数：
//   - 时钟周期：10ns（100MHz）
//   - 总仿真时间：约 25.6 μs
//   - 波形格式：VCD（可用 GTKWave 打开）
//
// 频率控制字计算：
//   公式：phase_inc = f_out × 2^48 / f_clk
//
//   1MHz 控制字：
//     phase_inc = 1,000,000 × 2^48 / 100,000,000
//               = 2^48 / 100
//               = 2,814,749,767,106.56...
//     取整：48'h28f5c28f5c2 = 2,814,749,767,106
//
//   2MHz 控制字：1MHz 控制字 × 2 = 48'h51eb851eb84
//------------------------------------------------------------------------------

`timescale 1ns / 1ps  // 时间单位：1ns，时间精度：1ps

module tb_dds_compiler;

    //--------------------------------------------------------------------------
    // 【参数定义】
    //
    // CLK_PERIOD      : 时钟周期 10ns = 100MHz
    // PHASE_INC_1MHZ  : 1MHz 输出对应的 48-bit 频率控制字
    //                   计算：2^48 / 100 = 0x28f5c28f5c2
    //--------------------------------------------------------------------------
    localparam CLK_PERIOD = 10;                    // 100MHz = 10ns 周期
    localparam PHASE_INC_1MHZ = 48'h28f5c28f5c2;   // (1MHz/100MHz)×2^48

    //--------------------------------------------------------------------------
    // 【信号声明】
    //
    // clk        : 100MHz 时钟（由 testbench 产生）
    // rst_n      : 低电平复位（由 testbench 产生）
    // freq_load  : 频率加载脉冲（testbench 控制）
    // phase_inc  : 48-bit 频率控制字（testbench 设置）
    // sine_out   : DDS 输出的 16-bit 正弦波
    // out_valid  : DDS 输出有效标志
    //--------------------------------------------------------------------------
    reg         clk;         // 时钟
    reg         rst_n;       // 复位（低有效）
    reg         freq_load;   // 频率加载脉冲
    reg  [47:0] phase_inc;   // 频率控制字
    wire [15:0] sine_out;    // 正弦波输出
    wire        out_valid;   // 输出有效

    //--------------------------------------------------------------------------
    // 【时钟产生】100MHz = 10ns 周期
    //
    // forever 语句：无限循环，每半个周期翻转一次时钟
    // #(CLK_PERIOD/2) = #5ns，即时钟每 5ns 翻转 → 周期 10ns → 100MHz
    //--------------------------------------------------------------------------
    initial begin
        clk = 1'b0;                    // 初始状态：低电平
        forever #(CLK_PERIOD/2) clk = ~clk;  // 每 5ns 翻转 → 10ns 周期
    end

    //--------------------------------------------------------------------------
    // 【复位产生】低电平复位，持续 10 个时钟周期（100ns）
    //
    // 时序：
    //   t=0    : rst_n = 0（复位开始）
    //   t=100ns: rst_n = 1（复位释放，开始正常工作）
    //--------------------------------------------------------------------------
    initial begin
        rst_n = 1'b0;                  // 初始复位
        #(CLK_PERIOD * 10);            // 等待 100ns（10个时钟周期）
        rst_n = 1'b1;                  // 释放复位
    end

    //--------------------------------------------------------------------------
    // 【被测模块实例化】DUT (Device Under Test)
    //
    // 实例化 dds_compiler_wrapper，连接所有信号
    //--------------------------------------------------------------------------
    dds_compiler_wrapper dut (
        .clk       (clk),
        .rst_n     (rst_n),
        .freq_load (freq_load),
        .phase_inc (phase_inc),
        .sine_out  (sine_out),
        .out_valid (out_valid)
    );

    //--------------------------------------------------------------------------
    // 【测试激励】主测试流程
    //
    // 测试步骤：
    //   Step 1: 初始化，等待复位释放
    //   Step 2: 加载 1MHz 频率，观察 10μs（100个周期）
    //   Step 3: 加载 2MHz 频率，观察 10μs（200个周期）
    //   Step 4: 加载 0Hz（DC），观察 5μs
    //   Step 5: 结束仿真
    //--------------------------------------------------------------------------
    initial begin
        //----------------------------------------------------------------------
        // Step 1: 初始化
        //----------------------------------------------------------------------
        freq_load = 1'b0;              // 初始不加载频率
        phase_inc = 48'd0;             // 初始频率控制字为 0

        // 等待复位释放（rst_n 从 0 变 1）
        @(posedge rst_n);
        #(CLK_PERIOD * 2);             // 再等待 2 个时钟周期，确保稳定

        //----------------------------------------------------------------------
        // Step 2: 加载 1MHz 频率
        //----------------------------------------------------------------------
        phase_inc = PHASE_INC_1MHZ;    // 设置频率控制字为 1MHz
        freq_load = 1'b1;              // 产生加载脉冲（高电平）
        @(posedge clk);                // 等待一个时钟上升沿
        freq_load = 1'b0;              // 脉冲结束（低电平）

        // 等待 DDS 稳定并产生输出
        #(CLK_PERIOD * 50);            // 等待 50 个时钟周期（500ns）

        // 检查 out_valid 是否变高
        if (out_valid !== 1'b1) begin
            $display("[ERROR] out_valid not asserted after startup");
        end else begin
            $display("[PASS] out_valid is high");
        end

        // 观察 1MHz 正弦波输出 10μs（1000 个时钟周期）
        // 1MHz 周期 = 1μs，10μs 内约有 10 个完整周期
        $display("Monitoring 1MHz sine output for 10us...");
        repeat (1000) begin
            @(posedge clk);            // 等待 1000 个时钟上升沿 = 10μs
        end

        //----------------------------------------------------------------------
        // Step 3: 加载 2MHz 频率
        //----------------------------------------------------------------------
        $display("\nLoading 2MHz frequency...");
        phase_inc = PHASE_INC_1MHZ * 2; // 频率控制字 ×2 = 2MHz
        freq_load = 1'b1;               // 产生加载脉冲
        @(posedge clk);
        freq_load = 1'b0;               // 脉冲结束

        // 观察 2MHz 正弦波输出 10μs（1000 个时钟周期）
        // 2MHz 周期 = 0.5μs，10μs 内约有 20 个完整周期
        $display("Monitoring 2MHz sine output for 10us...");
        repeat (1000) begin
            @(posedge clk);            // 等待 1000 个时钟上升沿 = 10μs
        end

        //----------------------------------------------------------------------
        // Step 4: 加载 0Hz（DC，直流）
        //----------------------------------------------------------------------
        $display("\nLoading 0Hz (DC)...");
        phase_inc = 48'd0;             // 频率控制字 = 0 → 输出恒定值
        freq_load = 1'b1;              // 产生加载脉冲
        @(posedge clk);
        freq_load = 1'b0;              // 脉冲结束

        // 观察 DC 输出 5μs（500 个时钟周期）
        $display("Monitoring DC output for 5us...");
        repeat (500) begin
            @(posedge clk);            // 等待 500 个时钟上升沿 = 5μs
        end

        //----------------------------------------------------------------------
        // Step 5: 仿真结束
        //----------------------------------------------------------------------
        $display("\n[TEST COMPLETE] DDS Compiler wrapper simulation finished");
        $finish;                        // 结束仿真
    end

    //--------------------------------------------------------------------------
    // 【超时看门狗】防止仿真死锁
    //
    // 如果主测试流程在 5000 个时钟周期（50μs）内没有结束，
    // 看门狗会打印错误并强制结束仿真。
    //--------------------------------------------------------------------------
    initial begin
        #(CLK_PERIOD * 5000);          // 等待 50μs
        $display("[ERROR] Simulation timeout!");
        $finish;
    end

    //--------------------------------------------------------------------------
    // 【波形输出】生成 VCD 文件供 GTKWave 查看
    //
    // VCD（Value Change Dump）是标准的波形文件格式。
    // 可以用 GTKWave 或 Vivado 的波形查看器打开。
    // 文件路径：仿真目录下的 dds_compiler_tb.vcd
    //--------------------------------------------------------------------------
    initial begin
        $dumpfile("dds_compiler_tb.vcd");  // 设置 VCD 文件名
        $dumpvars(0, tb_dds_compiler);      // 记录所有信号
    end

endmodule
