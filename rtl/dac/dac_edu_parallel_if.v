//------------------------------------------------------------------------------
// DAC Education Board Parallel Interface (3PD9708E)
// 【教学 DAC 并行接口适配模块】
//
`timescale 1ns / 1ps
//
// 功能说明：
//   将内部 16-bit 有符号正弦样本转换为教学 DAC 需要的 8-bit 偏移码，
//   并产生 DAC 采样时钟。
//
// 适配 DAC 芯片：正点原子 ATK-HS-ADDA 模块上的 3PD9708E（8bit 125MSPS）
//
// 数据格式转换：
//   内部样本：16-bit 有符号补码，范围 [-32768, +32767]
//   DAC 输入：8-bit 无符号偏移码，范围 [0, 255]
//   转换方法：取高 8 位后加 128（相当于把零点移到 128）
//
// 时钟说明：
//   da_clk = ~clk，让 DAC 在 FPGA 时钟下降沿附近采样，建立保持时间更充裕
//------------------------------------------------------------------------------

module dac_edu_parallel_if (
    input  wire        clk,      // FPGA 内部时钟（当前 100MHz）
    input  wire        rst_n,    // 低电平复位
    input  wire [15:0] sine_in,  // 16-bit 有符号正弦样本
    output wire        da_clk,   // DAC 采样时钟
    output wire [7:0]  da_data   // DAC 并行数据（8bit）
);

    //--------------------------------------------------------------------------
    // 数据格式转换：有符号补码 -> 无符号偏移码
    //
    // sine_in[15:8] 是原始样本的高 8 位，范围映射：
    //   sine_in = -32768 (0x8000) -> sine_in[15:8] = 0x80 = 128
    //   sine_in = 0      (0x0000) -> sine_in[15:8] = 0x00 = 0
    //   sine_in = +32767 (0x7FFF) -> sine_in[15:8] = 0x7F = 127
    //
    // 加 128 后：
    //   -32768 -> {~1, 0000000} = 10000000 -> 128? 不对，让我重新算：
    //   sine_in = 0x8000: [15]=1, [14:8]=0000000 -> {~1, 0000000} = {0, 0000000} = 0
    //   sine_in = 0x0000: [15]=0, [14:8]=0000000 -> {~0, 0000000} = {1, 0000000} = 128
    //   sine_in = 0x7FFF: [15]=0, [14:8]=1111111 -> {~0, 1111111} = {1, 1111111} = 255
    //
    // 标准做法：直接对最高位取反（有符号补码转偏移码）
    //--------------------------------------------------------------------------
    assign da_data = {~sine_in[15], sine_in[14:8]};

    //--------------------------------------------------------------------------
    // DAC 时钟：与系统时钟反相
    //
    // 原因：3PD9708E 在 clk 上升沿锁存数据。
    // 如果 da_clk = clk，FPGA 输出数据变化和时钟上升沿几乎同时发生，
    // 可能导致建立时间不够。反相后，DAC 在 FPGA 时钟下降沿后采样，
    // 数据已经稳定，时序更安全。
    //--------------------------------------------------------------------------
    assign da_clk = ~clk;

endmodule
